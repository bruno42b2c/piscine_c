int match(char *s1, char *s2)
{
	int retour;

	while(*s1)
	{ 
	retour = 0;
		if ((*s1 == *s2) || (*s2 == '*'))
				{	
				retour = 1; 
					while (*s2)	
					{
						if ((*s2 == '*') && (*s1 != '\0'))
						{
						s1++;
						retour = 1; 
						}	
						if ((*s2 == '*') && (*s1 = '\0'))
						{
						s1++;
						retour = 1; 
						}
					s2++;	
					}
				}	
	s1++;
	}
	return (retour);
}
