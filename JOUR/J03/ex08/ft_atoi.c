int	ft_atoi(char *str)
{
	int		nb;
	char	sign;

	nb = 0;
	while (*str)
	{
		if (str[0] == '-')
			sign = '-';
		else
		{
			if ((*str >= '0') && (*str <= '9'))
			{
				nb = nb * 10;
				nb = nb + ((*str) - 48);
			}
			else
				break ;
		}
		str++;
	}
	return ((sign == '-') ? nb * -1 : nb);
}
